import java.util.Arrays;
import java.util.Vector;

public class VectorExample {
    public static void main(String[] args) {

        // vector only declared
        Vector<Integer> vector;
        // vector assigned to a new, empty vector
        vector = new Vector<>();
        // vector assigned to a list of values
        vector = new Vector<>(Arrays.asList(1, 2, 3));


    }
}
